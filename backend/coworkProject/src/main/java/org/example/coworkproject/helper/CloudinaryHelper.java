package org.example.coworkproject.helper;

import com.cloudinary.Cloudinary;
import com.cloudinary.utils.ObjectUtils;
import jakarta.annotation.PostConstruct;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Component;
import org.springframework.web.multipart.MultipartFile;

import java.io.File;
import java.io.FileOutputStream;
import java.io.IOException;
import java.util.Map;

@Component
public class CloudinaryHelper {
    private  Cloudinary cloudinary;
    @Value("${api.cloudinary.name}")
    private String cloudName;

    @Value("${api.cloudinary.api-key}")
    private String apiKey;

    @Value("${api.cloudinary.api-secret}")
    private String apiSecret;


    @PostConstruct
    public void init() {
        cloudinary = new Cloudinary(ObjectUtils.asMap(
                "cloud_name", cloudName,
                "api_key", apiKey,
                "api_secret", apiSecret,
                "secure", true
        ));
    }

   public Map upload(MultipartFile mpf) throws IOException {
       File file = convert(mpf);
       Map result = cloudinary.uploader().upload(file, ObjectUtils.asMap("folder", "coworked-space/"));
       file.delete();
       return result;
    }

    public Map delete(String id) throws IOException {
        Map result = cloudinary.uploader().destroy(id, ObjectUtils.emptyMap());
        return result;
    }

    private File convert(MultipartFile multipartFile) throws IOException {
        File file = new File(multipartFile.getOriginalFilename());
        FileOutputStream fo = new FileOutputStream(file);
        fo.write(multipartFile.getBytes());
        fo.close();
        return file;
    }
}